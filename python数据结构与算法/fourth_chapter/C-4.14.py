'''
A，B，C三个圆柱，分别为初始位，过渡位，目标位，设A柱为初始位，C位为最终目标位

（1）将最上面的n-1个圆盘从初始位移动到过渡位

（2）将初始位的最底下的一个圆盘移动到目标位

（3）将过渡位的n-1个圆盘移动到目标位
'''


def move(n, a, b, c):
    if n == 1:
        print(a, "->", c)
    else:
        move(n-1, a, c, b)
        move(1, a, b, c)
        move(n-1, b, a, c)


if __name__ == '__main__':
    move(3, "a", "b", "c")
